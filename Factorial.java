package week1.day1;



public class Factorial {
	

	public static void main(String[] args) {
		// Declare your input as 5
		int num=5;
		int fact=1;
		
		// Declare an integer variable fact as 1
		

		for (int i=1; i<=num; i++) 
		{
			fact=fact*i;
			
		}
		System.out.println(fact);

		// Print factorial (fact)
	

	}

}
